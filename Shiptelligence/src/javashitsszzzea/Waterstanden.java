package javashitsszzzea;

public class Waterstanden {
	private String tijd, waarde;
	Waterstanden(String tijd, String waarde){
		this.tijd = tijd;
		this.waarde = waarde;
	}
	public String getTijd() {
		return tijd;
	}
	public void setTijd(String tijd) {
		this.tijd = tijd;
	}
	public String getWaarde() {
		return waarde;
	}
	public void setWaarde(String waarde) {
		this.waarde = waarde;
	}
	
}
